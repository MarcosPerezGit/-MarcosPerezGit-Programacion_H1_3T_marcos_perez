package paquete;

public class Adopcion {
    public String nombreAdoptante; // Almacena el nombre del adoptante
    public String dniAdoptante;    // Almacena el DNI del adoptante

    // Constructor que inicializa los atributos con los valores recibidos
    public Adopcion(String nombreAdoptante, String dniAdoptante) {
        this.nombreAdoptante = nombreAdoptante; // Asigna el nombre del adoptante
        this.dniAdoptante = dniAdoptante;       // Asigna el DNI del adoptante
    }
}


